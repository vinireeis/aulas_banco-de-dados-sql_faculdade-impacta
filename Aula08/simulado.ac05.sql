SELECT CLIENTE.NOME, COUNT(*) AS QTD_PEDIDOS FROM CLIENTE 
JOIN PEDIDO ON CLIENTE.CODCLI = PEDIDO.CODCLI 
WHERE MONTH(PEDIDO.DATA) = 1 
GROUP BY CLIENTE.NOME 
HAVING COUNT(*) > 100;